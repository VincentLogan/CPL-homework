/*
    程序功能：生成两棵斜链状二叉搜索树（BST）的测试数据
    输出格式：
       第一行：第一棵树的结点数 N1
       接下来 N1 行：每行两个整数 key 和 parent_index（-1 表示根结点）
       接着一行：第二棵树的结点数 N2
       接下来 N2 行：每行两个整数 key 和 parent_index
       最后一行：目标和 N
        
       树结构说明：
         两棵树均满足二叉搜索树性质（左小右大）
 */
#include <stdio.h>

int main() {
    int N1 = 100000; // 第一棵树规模
    int N2 = 100000; // 第二棵树规模
    
    // 第一棵树
    printf("%d\n", N1);
    for (int i = 0; i < N1; i++) {
        // 第一个结点（i=0）为根，parent = -1
        // 后续结点的父结点为前一个结点（i-1）
        printf("%d %d\n", i * 2, i - 1); 
    }

    // 第二棵树
    printf("%d\n", N2);
    for (int i = 0; i < N2; i++) {
        printf("%d %d\n", i * 3, i - 1);
    }
    
    /// 目标和 target 介于两棵树的最小值和最大值之间
    // 第一棵树最小值 0，最大值 (N1-1)*2
    // 第二棵树最小值 0，最大值 (N2-1)*3
    printf("%d\n", 50000);


    return 0;
}